        public static Transliterated Transliterate(Transliterated tr, string lang)
        {
            string word = "";
            if (lang == "am")
            {
                tr.counter.Clear();
                for (int i = 0; i < tr.word.Length; i++)
                {
                    if (LookupTable.ContainsKey(tr.word[i]))
                    {
                        word += LookupTable[tr.word[i]];
                        tr.counter.Add(LookupTable[tr.word[i]].Length);
                    }
                }
            }
            else if (lang == "en")
            {
                int j = 0;
                for (int i = 0; i < tr.counter.Count; i++)
                {
                    if (j + tr.counter[i] > tr.word.Length)
                    {
                        tr.counter[i] = tr.word.Length - j;
                    }
                    var enLetter = LookupTable.FirstOrDefault(x => x.Value == tr.word.Substring(j, tr.counter[i])).Key;
                    j += tr.counter[i];
                    if (enLetter != default)
                    {
                        word += enLetter;
                    }
                }
            }
            tr.word = word;
            return tr;
        }

        public static string Stem(string word)
        {
            List<string> a = new();
            Transliterated tr = new();
            tr.word = word;
            tr = Transliterate(tr, "am");
            if (AmharicWordlist.ContainsKey(tr.word))
                return Transliterate(tr, "en").word;
            //affix removal
            //CvCv checking
            char next = 'c';
            char vowel = ':';
            string cv = "";
            if (AmharicVowels.Contains(tr.word[tr.word.Length - 1].ToString()))//if it ends with a vowel
                for (int i = 0; i < tr.word.Length; i++)
                {
                    if (next == 'c')
                    {
                        cv += tr.word[i];
                        next = 'v';
                    }
                    else
                    {
                        if (vowel == ':')//check beginning
                            if (AmharicVowels.Contains(tr.word[i].ToString()))
                            {
                                vowel = tr.word[i];
                                cv += 'e';
                            }
                            else
                                break;
                        else if (tr.word[i] == vowel && AmharicVowels.Contains(tr.word[i].ToString()))
                            cv += 'e';
                        else break;
                        next = 'c';
                    }
                    if (i == tr.word.Length - 1)
                    {
                        tr.word = cv;
                    }
                }
            if (AmharicWordlist.ContainsKey(tr.word))
                return Transliterate(tr, "en").word;
            //CvC checking
            string cc, vv;
            cv = "";
            for (int i = 0; i < tr.word.Length; i++)
            {
                if (!AmharicVowels.Contains(tr.word[i].ToString()) && i + 1 < (tr.word.Length - 1))
                {
                    // Console.Write(i + " ");
                    cc = tr.word[i].ToString();
                    i++;
                    if (AmharicVowels.Contains(tr.word[i].ToString()))
                    {
                        vv = tr.word[i].ToString();
                        i++;
                        // Console.WriteLine(tr.word[i]);
                        // Console.WriteLine(tr.word.Length - 1 + " " + i);
                        //Console.WriteLine(cc);
                        if (i <= (tr.word.Length - 1) && !AmharicVowels.Contains(tr.word[i].ToString()) && tr.word[i].ToString() == cc)
                        {

                            tr.word = tr.word.Remove(i - 1, 2);

                        }
                    }

                }
            }

            for (int i = 0; i < Prefixes.Count; i++)
            {
                if (AmharicWordlist.ContainsKey(tr.word))
                    return Transliterate(tr, "en").word;
                if (tr.word.StartsWith(Prefixes[i]))
                {
                    tr.word = tr.word.Substring(Prefixes[i].Length);
                    for (int j = 0; j < prefix_size[i]; j++)
                    {
                        tr.counter.RemoveAt(0);
                    }
                    i = 0;
                }
            }
            for (int i = 0; i < Suffixes.Count; i++)
            {
                if (AmharicWordlist.ContainsKey(tr.word))
                    return Transliterate(tr, "en").word;
                if (tr.word.EndsWith(Suffixes[i]))
                {
                    tr.word = tr.word.Substring(0, tr.word.Length - Suffixes[i].Length);
                    if (tr.counter[tr.counter.Count - 1] <= Suffixes[i].Length)
                        tr.counter.RemoveAt(tr.counter.Count - 1);
                    else
                        tr.counter[tr.counter.Count - 1] -= Suffixes[i].Length;
                    i = 0;
                }
            }
            return Transliterate(tr, "en").word;
        }
